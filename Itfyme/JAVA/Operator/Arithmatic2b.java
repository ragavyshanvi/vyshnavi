
import java.util.Scanner;
public class Arithmatic2b{
    public static void main(String [] args){
        int a=Integer.parseInt(args[0]);
        int b=Integer.parseInt(args[1]);
        System.out.println(a+b); 
        System.out.println(a-b); 
        System.out.println(a*b); 
        System.out.println(a/b); 
        System.out.println(a%b); 
    }
}
    